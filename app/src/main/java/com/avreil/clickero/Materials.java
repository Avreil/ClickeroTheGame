package com.avreil.clickero;

public class Materials {

    Integer Stone,Wood;

    public Materials(){
        Stone=0;
        Wood=0;
    }

    public Integer getStone() {
        return Stone;
    }

    public void setStone(Integer stone) {
        Stone = stone;
    }

    public Integer getWood() {
        return Wood;
    }

    public void setWood(Integer wood) {
        Wood = wood;
    }

}
